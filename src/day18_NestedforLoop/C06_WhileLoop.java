package day18_NestedforLoop;

public class C06_WhileLoop {
    public static void main(String[] args) {
        // 40 ile 60 arasını yazdır

        int baslangic = 40, bitis = 60;
        int i=baslangic;
        while (i<=bitis) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println();
    }
}
