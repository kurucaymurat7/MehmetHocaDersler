package day04_dataCasting;

public class C05_Increment_Decrement {
    public static void main(String[] args) {
        int sayi1=20;
        System.out.println(sayi1+10); //30 yazdırır.
        System.out.println(sayi1);//20 yazar.

        sayi1+=10;
        System.out.println(sayi1+=10);//30

        System.out.println(sayi1++);
    }
}
